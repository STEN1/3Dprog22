Kube::Kube(GameObject& parent,Shader& shader, uint32_t texture, glm::mat4 transform)
    : VisualObject(shader, parent, transform)
    , m_texture{ texture }
{
    m_isTransparent = false;
    glGenVertexArrays(1, &m_VAO);
    glBindVertexArray(m_VAO);
    glGenBuffers(1, &m_VBO);
    glBindBuffer(GL_ARRAY_BUFFER, m_VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(s_cubeVertexData), s_cubeVertexData, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);

    // This probably causes the following warning on Nvidia drivers.
    // "Program/shader state performance warning: Vertex shader in program [] is being recompiled based on GL state."
    glVertexAttrib3f(1, 1.f, 1.f, 1.f);
    glDisableVertexAttribArray(1);

    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(2);
}

Kube::~Kube()
{
    glDeleteVertexArrays(1, &m_VAO);
}

void Kube::Draw()
{
    glDisable(GL_CULL_FACE);
    m_shader.Use();
    m_shader.SetMat4f(m_globalTransform, "model");
    glBindTexture(GL_TEXTURE_2D, m_texture);
    glBindVertexArray(m_VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);
    glEnable(GL_CULL_FACE);
}